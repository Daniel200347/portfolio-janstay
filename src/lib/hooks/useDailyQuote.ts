export function useDailyQuote() {
    const [quote, setQuote] = useState("");

    useEffect(() => {
        const shownIndexesStr = localStorage.getItem("shownQuotes");
        let shownIndexes: number[] = shownIndexesStr ? JSON.parse(shownIndexesStr) : [];

        if (shownIndexes.length >= quotes.length) {
            shownIndexes = [];
        }

        const availableIndexes = quotes
            .map((_, index) => index)
            .filter((index) => !shownIndexes.includes(index));

        const randomIndex = availableIndexes[Math.floor(Math.random() * availableIndexes.length)];

        shownIndexes.push(randomIndex);
        localStorage.setItem("shownQuotes", JSON.stringify(shownIndexes));

        setQuote(quotes[randomIndex]);
    }, []);

    return quote;
}

